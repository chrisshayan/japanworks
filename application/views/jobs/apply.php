<script>
    function toggleCVform(z) {
        if (z == 1) {
            $("#attachCV").hide();
            $("#noCV").slideDown(400);
            $("#cancel_btn").show();
        }
        else if (z == 0) {
            $("#attachCV").show();
            $("#noCV").slideUp(400);
            $("#cancel_btn").hide();
        }
    }

    function showTab(id) {
        $('#myTab a[href="' + id + '"]').tab('show')
    }

    function showTopApply() {
        var sTop = $(document).scrollTop();
        if (sTop >= 400) {
            $("#topApply").show();
        }
        else {
            $("#topApply").hide();
        }
    }


    $(function() {
        var st = $("#title_area").html();

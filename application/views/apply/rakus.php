    $.validator.addMethod("customemail",
            function(value, element) {
                return /^(([^<>()[\]\\.,;:\s@\"]+(\.[^<>()[\]\\.,;:\s@\"]+)*)|(\".+\"))@((\[[0-9{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\])|(([a-zA-Z\-0-9]+\.)+[a-zA-Z]{2,}))$/.test(value);
            },
            "<?php echo "Please enter your email." ?>"
            );
    $.validator.addMethod("customephone",
            function(value, element) {
                return /^[0-9().-]+$/.test(value);
            },
            "<?php echo "Please enter your phone number." ?>"
            );
    $.validator.addMethod(
            "filesize",
            function(value, element) {

                if ($(element).attr('type') == "file"
                        && ($(element).hasClass('required')
                                || element.files.length > 0)) {
                    var size = 0;
                    var $form = $(element).parents('form').first();
                    var $fel = $form.find('input[type=file]');
                    var $max = $form.find('input[name=maxFileSize]').first();
                    if ($max) {
                        for (var j = 0, fo; fo = $fel[j]; j++) {
                            files = fo.files;
                            for (var i = 0, f; f = files[i]; i++) {
                                size += f.size;
                            }
                        }
                        return size <= $max.val();
                    }
                }
                return true;
            },
            "The file selected exceed size limit. Please choose another file."
            );


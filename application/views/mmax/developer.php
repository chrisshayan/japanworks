            function vaidateBeforeSubmit() {
                if ($("#frmSignUp").valid())
                {

                    $('#applyButton').prop('disabled', true);
                    setTimeout(function () {
                        $('#applyButton').prop('disabled', false);
                    }, 2000);
                    return true;
                }
                return false;
            }


            function toggleCVform(z) {
                if (z == 1) {
                    $('.check-option').val("true");
                    $("#attachCV").hide();
                    $("#noCV").slideDown(400);
                    $("#cancel_btn").show();
                }
                else if (z == 0) {
                    $('.check-option').val("false");
                    $("#attachCV").show();
                    $("#noCV").slideUp(400);
                    $("#cancel_btn").hide();
                }
            }

            function showTab(id) {
                $('#myTab a[href="' + id + '"]').tab('show');
            }

            function showTopApply() {
                var sTop = $(document).scrollTop();
                if (sTop >= 400) {
                    $("#topApply").show();
                }
                else {
                    $("#topApply").hide();
                }
            }


            $(function () {

                $(window).scroll(function () {
                    showTopApply();
                });

            });
            function scrollToAnchor(aid) {
                var aTag = $("a[name='" + aid + "']");
                $('html,body').animate({scrollTop: aTag.offset().top}, 'slow', function () {
                    $("#topApply").hide();
                });
            }

